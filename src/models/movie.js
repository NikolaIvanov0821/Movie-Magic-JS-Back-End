export class Movie {
    constructor(id, title, category, genre, director, year, imageURL, rating, description) {
        this.id = id;
        this.title = title;
        this.category = category;
        this.genre = genre;
        this.director = director;
        this.year = year;
        this.imageURL = imageURL;
        this.rating = rating;
        this.description = description;
    }
}